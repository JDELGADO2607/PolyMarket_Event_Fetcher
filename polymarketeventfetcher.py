                        yes_data[f'bid_{i}'] = float(ob_yes.bids[-i].price)
                        yes_data[f'ask_{i}'] = float(ob_yes.asks[-i].price)
                        yes_data[f'bidsize_{i}'] = float(ob_yes.bids[-i].size)
