            print(f"  NO:  {len(self.trades['NO'])} trades")
            print(f"  Total: {len(self.trades['YES']) + len(self.trades['NO'])} trades")

            if self.trades['YES']:
                yes_volume = sum(t['size'] for t in self.trades['YES'])
